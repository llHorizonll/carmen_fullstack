// Helper: generate monthly depreciation schedules for an asset
function generateSchedules(
  assetId: string,
  assetCode: string,
  assetName: string,
  startDate: string,
  openingCost: number,
  monthlyDep: number,
  salvageValue: number,
  currencyCode: string,
  months: number,
): DepreciationScheduleDto[] {
  const schedules: DepreciationScheduleDto[] = []
  let opening = openingCost
  let accumulated = 0
  const start = new Date(startDate)

  for (let i = 0; i < months; i++) {
    const schedDate = new Date(start)
    schedDate.setMonth(schedDate.getMonth() + i)
    const depAmount = Math.min(monthlyDep, opening - salvageValue)
    if (depAmount <= 0) break
    accumulated += depAmount
    const closing = opening - depAmount

    schedules.push({
      id: uuid(`ds${assetCode.toLowerCase()}`, i + 1),
      assetId,
      assetCode,
      assetName,
      fiscalPeriodId: uuid('fprd', schedDate.getFullYear() * 100 + schedDate.getMonth() + 1),
      fiscalPeriodName: `${schedDate.toLocaleString('en-US', { month: 'short' })} ${schedDate.getFullYear()}`,
      scheduleNumber: i + 1,
      scheduleDate: schedDate.toISOString().slice(0, 10) + 'T00:00:00Z',
      openingValue: Math.round(opening * 100) / 100,
      depreciationAmount: Math.round(depAmount * 100) / 100,
      depreciationAmountBase: Math.round(depAmount * 100) / 100,
      closingValue: Math.round(closing * 100) / 100,
      accumulatedDepreciation: Math.round(accumulated * 100) / 100,
      currencyCode,
      isPosted: i < months - 3, // last 3 months unposted
      journalVoucherId: i < months - 3 ? uuid('jvdp', i + 1) : undefined,
      journalVoucherNumber: i < months - 3 ? `JV-DEP-${String(i + 1).padStart(4, '0')}` : undefined,
      postedAt: i < months - 3 ? schedDate.toISOString() : undefined,
      postedBy: i < months - 3 ? 'system@grandparadise.com' : undefined,
      notes: undefined,
      createdAt: schedDate.toISOString(),
    })

    opening = closing
  }
  return schedules
}

