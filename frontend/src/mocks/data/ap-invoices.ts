// ---------------------------------------------------------------------------
// Unpaid AP Invoices (for payment allocation screens)
// ---------------------------------------------------------------------------

export const mockUnpaidApInvoices: UnpaidInvoiceDto[] = mockApInvoices
  .filter((inv) =>
    [ApInvoiceStatus.Approved, ApInvoiceStatus.PartiallyPaid].includes(inv.status),
  )
  .map((inv) => {
    const dueDateMs = new Date(inv.dueDate).getTime()
    const nowMs = new Date('2025-11-10').getTime()
    const daysOverdue = Math.max(0, Math.floor((nowMs - dueDateMs) / 86400000))
    return {
      id: inv.id,
      invoiceNumber: inv.invoiceNumber,
      vendorInvoiceNumber: inv.vendorInvoiceNumber,
      invoiceDate: inv.invoiceDate,
      dueDate: inv.dueDate,
      currencyCode: inv.currencyCode,
      totalAmount: inv.totalAmount,
      paidAmount: inv.paidAmount,
      balanceAmount: inv.balanceAmount,
      daysOverdue,
    }
  })
